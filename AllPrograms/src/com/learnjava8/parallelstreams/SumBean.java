package com.learnjava8.parallelstreams;

public class SumBean {
    private int sum;
    private int total;

    public int getSum() {
        return sum;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public  int sum(int number){
            return total+=number;
    }
}
